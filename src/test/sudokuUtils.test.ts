import { isValidMove, type SudokuGrid } from "../utils/sudokuUtils";

const board: SudokuGrid = [
  [5, 3, 0, 0, 7, 0, 0, 0, 0],
  [6, 0, 0, 1, 9, 5, 0, 0, 0],
  [0, 9, 8, 0, 0, 0, 0, 6, 0],
  [8, 0, 0, 0, 6, 0, 0, 0, 3],
  [4, 0, 0, 8, 0, 3, 0, 0, 1],
  [7, 0, 0, 0, 2, 0, 0, 0, 6],
  [0, 6, 0, 0, 0, 0, 2, 8, 0],
  [0, 0, 0, 4, 1, 9, 0, 0, 5],
  [0, 0, 0, 0, 8, 0, 0, 7, 9],
];

test('valid move returns true', () => {
  expect(isValidMove(board, 0, 2, 4)).toBe(true);
});

test('invalid move returns false', () => {
  expect(isValidMove(board, 0, 2, 5)).toBe(false);
});